a = 40
b = 5
print("Arithmetic Operators:")
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a % b =", a % b)

print("\nComparison Operators:")
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)
print("a >= b:", a >= b)
print("a <= b:", a <= b)

print("\nLogical Operators:")
x = True
y = False
print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)

print("\nBitwise Operators:")
print("a & b:", a & b)
print("a | b:", a | b)
print("a ^ b:", a ^ b)
print("~a:", ~a)

print("\nAssignment Operators:")
c = a
print("c =", c)
c += b
print("c += b:", c)
c -= b
print("c -= b:", c)

print("\nMembership Operators:")
my_list = [1, 2, 3, 10]
print("10 in my_list:", 10 in my_list)
print("5 not in my_list:", 5 not in my_list)

print("\nIdentity Operators:")
p = 5
q = 5
r = [1, 2, 3]
s = [1, 2, 3]
print("p is q:", p is q)
print("r is s:", r is s)
print("r == s:", r == s)
